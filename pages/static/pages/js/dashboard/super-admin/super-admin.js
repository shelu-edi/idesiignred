$(function () {

             var $testChart = $("#test-chart");
             $.ajax({
                 url: $testChart.data("url"),
                 success: function (data) {

                     var ctx = $testChart[0].getContext("2d");

                     new Chart(ctx, {
                         type: 'bar',
                         data: {
                             labels: data.labels,
                             datasets: [{
                                 label: 'TestData',
                                 backgroundColor: '#163172',
                                 data: data.data
                             }]
                         },
                         options: {
                             responsive: true,
                             legend: {
                                 position: 'top',
                             },
                             title: {
                                 display: true,
                                 text: 'Test Chart'
                             }
                         }
                     });

                 }
             });
         })

